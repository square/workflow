package com.squareup.sample.poetry.model

import com.squareup.sample.poetry.model.Poet.Poe

val TheConquerorWorm = Poem(
    title = "The Conqueror Worm",
    poet = Poe,
    stanzas = listOf(
        listOf(
            "Lo! ’t is a gala night",
            "\tWithin the lonesome latter years!",
            "An angel throng, bewinged, bedight",
            "\tIn veils, and drowned in tears,",
            "Sit in a theatre, to see",
            "\tA play of hopes and fears,",
            "While the orchestra breathes fitfully",
            "\tThe music of the spheres."
            ),
        listOf(
            "Mimes, in the form of God on high,",
            "\tMutter and mumble low,",
            "And hither and thither fly—",
            "\tMere puppets they, who come and go",
            "At bidding of vast formless things",
            "\tThat shift the scenery to and fro,",
            "Flapping from out their Condor wings",
            "\tInvisible Wo!"
            ),
        listOf(
            "That motley drama—oh, be sure",
            "\tIt shall not be forgot!",
            "With its Phantom chased for evermore",
            "\tBy a crowd that seize it not,",
            "Through a circle that ever returneth in",
            "\tTo the self-same spot,",
            "And much of Madness, and more of Sin,",
            "\tAnd Horror the soul of the plot."
            ),
        listOf(
            "But see, amid the mimic rout,",
            "\tA crawling shape intrude!",
            "A blood-red thing that writhes from out",
            "\tThe scenic solitude!",
            "It writhes!—it writhes!—with mortal pangs",
            "\tThe mimes become its food,",
            "And seraphs sob at vermin fangs",
            "\tIn human gore imbued."
            ),
        listOf(
            "Out—out are the lights—out all!",
            "\tAnd, over each quivering form,",
            "The curtain, a funeral pall,",
            "\tComes down with the rush of a storm,",
            "While the angels, all pallid and wan,",
            "\tUprising, unveiling, affirm",
            "That the play is the tragedy, “Man,”",
            "\tAnd its hero, the Conqueror Worm."
            )
    )
)
